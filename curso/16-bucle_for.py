# bucles - son estructuras de contros de flujo que repiten una o varias lineas de codigo

for num in range(0,20,2):
    print ("valor actual: {0}".format(num))

for i in range(1,13):
    print ("{0} x {1} = {2}".format(i, i, (i*i)))

for non in ["karen", "orcar", "loenardo", "jose"]:
    print("cantidad de letras de {0} es {1}".format(non, len(non)))